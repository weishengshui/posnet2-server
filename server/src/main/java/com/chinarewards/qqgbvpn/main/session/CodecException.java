package com.chinarewards.qqgbvpn.main.session;

/**
 * @author Cyril
 * 
 */
public class CodecException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -895726743325215929L;

	/**
	 * 
	 */
	public CodecException() {
		super();
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public CodecException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * @param arg0
	 */
	public CodecException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public CodecException(Throwable arg0) {
		super(arg0);
	}

}
